            <h2>Morotskaka</h2>
            <p>Den perfekta morotskakan enligt mig ska vara hög, sådär saftig så att den bara smälter i munnen, vara kryddig och ha generöst med frosting. Dessutom så är motoskakan en sån kaka som passar att servera till alla tillfällen. Såväl fikastunden i vardagen som till kalaset</p>
            <p>Recept nedan</p>
            <p><img src="https://i2.wp.com/mykitchenstories.se/app/uploads/sites/2/2021/02/IMG_0279-scaled.jpg?resize=1229%2C1536&ssl=1" alt="Bild på morotskaka från Camilla Hamid" height="600" width="620"> </p>
            <p>Receptet bakas i en långpanna med måtten 30×40 cm, vilket ger en morotskaka på ca 24 generösa bitar.</p>
            <p>Kaka:</p>
            <ul class="recept">
                <li>300 g morötter (4-5 medelstora morötter)</li>
                <li>1 dl farinsocker</li>
                <li>2 msk malen kanel</li>
                <li>1 msk malen kardemumma</li>
                <li>2 tsk malen ingefära</li>
                <li>6 ägg</li>
                <li>5 dl strösocker</li>
                <li>2,5 dl neutral olja (raps-, mat- eller solrosolja)</li>
                <li>1 msk vaniljsocker</li>
                <li>1 msk bakpulver</li>
                <li>6 dl vetemjöl</li>

            </ul>
            <p>Frosting</p>
            <ul class="recept">
                <li>600 g färskost</li>
                <li>300 g rumstempererat smör</li>
                <li>2 tsk vaniljsocker</li>
                <li>4 dl florsocker</li>
                <li> rivet limeskal från 2 lime att dekorera med</li>

            </ul>
        </div>

        <div class="recept-st2">
            <p>Gör så här:</p>
            <ol class="do">
                <li> Sätt ugnen på 175 grader (över- och undervärme) och täck din långpanna med bakplåtspapper.</li>
                <li>Skala morötterna och riv dem fint på ett rivjärn. I en bunke blandar du ihop rivna morötter, farinsocker, kanel, kardemumma och ingefära. Ställ åt sidan för att dra medan du gör resten av smeten. Detta kommer att göra så att smakerna av kryddorna kommer fram extra mycket med hjälp av vätskan från morötterna.</li>
                <li>Ta fram en ny bunke som du vispar ägg och strösocker i, vitt och pösigt med elvisp. Ha sedan ner blandningen med rivna morötter och neutral olja. Blanda försiktigt ihop till en jämn smet.</li>
                <li>Sikta ner vaniljsocker, bakpulver och vetemjöl. Vänd försiktigt ihop till en jämn smet med slickepott.</li>
                <li>Häll upp smeten i långpannan och grädda kakan i nedre delen av ugnen i 40-45 minuter eller tills kakan är helt genomgräddad. Låt kakan svalna helt och hållet i rumstemperatur innan du brer på frosting.</li>
                <li>Frostingen gör du genom att vispa ihop färskost och smör riktigt krämigt med elvisp i en bunke. </li>
                <li>Sikta ner florsockret och vaniljsocker. Vänd försiktigt ihop till en jämn och krämig frosting med slickepott. Det är viktigt att du verkligen försiktigt vänder ner florsockret och inte blandar för hårt för då kommer frostingen att bli lös. Om du upplever att din frosting ändå blir lös kan du ställa in den i kylskåpet en stund för att tjockna.</li>
                <li>Bre ut frostingen på kakan när den svanat och toppa med det yttersta skalet från 2 limefrukter.</li>
            </ol>
